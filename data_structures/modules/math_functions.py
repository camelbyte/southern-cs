# These are pretty self-explanatory, hence the lack of documentation. 

def squared(base):
    return base * base 


def subtract(a, b):
    return a - b


def add(a, b):
    return float(a + b)


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def square_root(a):
    return math.sqrt(a)


def power(a, b):
    return a ** b


def factorial(a):
    return math.factorial(a)


def sin(a):
    return math.sin(a)


def cos(a):
    return math.cos(a)


